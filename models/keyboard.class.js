/**
 * Class for managing keyboard state.
 */
class Keyboard {
    /**
     * @type {boolean} True if the left arrow key is pressed, otherwise false.
     */
    LEFT = false;

    /**
     * @type {boolean} True if the right arrow key is pressed, otherwise false.
     */
    RIGHT = false;

    /**
     * @type {boolean} True if the up arrow key is pressed, otherwise false.
     */
    UP = false;

    /**
     * @type {boolean} True if the down arrow key is pressed, otherwise false.
     */
    DOWN = false;

    /**
     * @type {boolean} True if the space key is pressed, otherwise false.
     */
    SPACE = false;

    /**
     * @type {boolean} True if the 'D' or 'd' key is pressed, otherwise false.
     */
    D = false;
}
